void USART3_IRQHandler(void) 
{
	static u8 i=0;
	
	if(USART_GetITStatus(USART3, USART_IT_RXNE))//接收中断(接收到的数据必须是0xAA开头   0xEE结尾)
	{	
		Res =USART_ReceiveData(USART3);//(USART1->DR);	//读取接收到的数据	
		if(Res==0xEE)//接收到帧尾
		{
			flag=0;//标志位复位
			i=0;//清零
			adcx=(int)USART_receive[0];//首位 adc数据
			pitch=(int)((USART_receive[1]<<8)+USART_receive[2])-180;//还原并保存上位机原始数据
			roll=(int)((USART_receive[3]<<8)+USART_receive[4])-180;	
			yaw=(int)((USART_receive[5]<<8)+USART_receive[6])-180;	
			Pwm_control();
			ServoPwmDutyHaveChange=1;//pwm变化标志位置1
			printf("wuwuwu%d",pitch);

		}
		if(flag==1)//接收数据
		{
		  USART_receive[i++]=Res;
		}
		if(Res==0xAA)//接收到帧头
		{
			flag=1;//标志位置1
		}
	}
} 
